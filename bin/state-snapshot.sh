#!/usr/bin/env bash
# state-snapshot.sh -- periodic durable state snapshot.
# Collects all agent current.md summaries into MASTER-LEDGER.md and commits to git.
# Runs every 5 minutes via cron. Uses flock to avoid conflicts.
set -uo pipefail
TS=$(date -u +%Y-%m-%dT%H:%M:%SZ)
STATE="$HOME/.claude/state"
LEDGER="$STATE/MASTER-LEDGER.md"
GIT_LOCK="/tmp/claude-state-git.lock"

{
echo "# MASTER-LEDGER -- updated $TS"
echo "# Auto-generated by state-snapshot.sh. Do not edit."
echo ""
echo "## Agent States"
for d in "$STATE"/*/; do
    agent=$(basename "$d")
    [[ "$agent" == "bin" ]] && continue
    cur="$d/current.md"
    if [ -f "$cur" ]; then
        MTIME=$(stat -c %Y "$cur" 2>/dev/null || echo 0)
        NOW=$(date +%s)
        AGE=$(( NOW - MTIME ))
        [ "$AGE" -gt 259200 ] && continue
        updated=$(grep 'UPDATED:' "$cur" 2>/dev/null | head -1 | sed 's/UPDATED://;s/^ *//')
        next=$(grep -A1 'NEXT_ACTION\|NEXT ACTION\|IN_PROGRESS' "$cur" 2>/dev/null | grep -v 'NEXT_ACTION\|NEXT ACTION\|IN_PROGRESS' | head -1 | cut -c1-100)
        compact=$(grep 'LAST_COMPACTED:' "$cur" 2>/dev/null | head -1 | sed 's/LAST_COMPACTED://;s/^ *//')
        echo "- **$agent** updated:${updated:-?} compacted:${compact:-never}"
        [ -n "$next" ] && echo "  next: $next"
    fi
done
echo ""
echo "## How to find everything"
echo "- Agent state: cat ~/.claude/state/<agent>/current.md"
echo "- Last compact: tail -30 ~/.claude/state/<agent>/compact-save.log"
echo "- Manual find: grep -r 'NEXT_ACTION' ~/.claude/state/"
} > "$LEDGER"

cd "$STATE"
# Portable atomic lock: prefer flock (Linux); fall back to mkdir-atomic (macOS/no-flock).
LOCK_DIR="${GIT_LOCK}.d"
if [ -d "$LOCK_DIR" ] && find "$LOCK_DIR" -maxdepth 0 -mmin +2 2>/dev/null | grep -q .; then
    rmdir "$LOCK_DIR" 2>/dev/null || true
fi
if command -v flock >/dev/null 2>&1; then
    exec 9>"$GIT_LOCK"
    if ! flock -x -w 20 9 2>/dev/null; then
        echo "$(date -u +%FT%TZ) state-snapshot: could not acquire git lock, skipping" >&2; exit 0
    fi
else
    _lk=0
    while ! mkdir "$LOCK_DIR" 2>/dev/null; do
        _lk=$(( _lk + 1 ))
        [ "$_lk" -ge 20 ] && { echo "$(date -u +%FT%TZ) state-snapshot: could not acquire git lock, skipping" >&2; exit 0; }
        sleep 1
    done
    trap "rmdir '$LOCK_DIR' 2>/dev/null || true" EXIT INT TERM
fi

git add -A
if ! git diff --cached --quiet; then
    git -c user.email=snapshot@local -c user.name=StateSnapshot commit -qm "snapshot $TS" 2>/dev/null
    for i in 1 2 3; do
        git push -q origin main 2>/dev/null && break
        git pull -q --rebase --autostash origin main 2>/dev/null || git rebase --abort 2>/dev/null
    done
fi
echo "$(date -u +%FT%TZ) state-snapshot: done"
