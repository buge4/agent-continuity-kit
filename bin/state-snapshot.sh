#!/usr/bin/env bash
# state-snapshot.sh -- periodic durable state snapshot.
# Collects all agent current.md summaries into MASTER-LEDGER.md and commits to git.
# Merge-union: entries from the old ledger are carried forward if current.md is missing or
# too old (< 72h since last ledger update), so entries are NEVER stripped unexpectedly.
# Portable atomic lock: flock on Linux, mkdir-atomic fallback on macOS.
set -uo pipefail
TS=$(date -u +%Y-%m-%dT%H:%M:%SZ)
STATE="$HOME/.claude/state"
LEDGER="$STATE/MASTER-LEDGER.md"
GIT_LOCK="/tmp/claude-state-git.lock"

# Build ledger using Python3 for merge-union logic
python3 - "$STATE" "$LEDGER" "$TS" <<'PY'
import sys, os, re, time, datetime
from pathlib import Path

state_dir, ledger_path, ts = sys.argv[1], sys.argv[2], sys.argv[3]
now = time.time()
MAX_AGE = 259200  # 72h

def parse_ts(s):
    """Parse an ISO 8601 timestamp string to a UTC epoch float. Returns None on failure."""
    s = s.strip().rstrip('Z')
    # Trim to at most 19 chars (YYYY-MM-DDTHH:MM:SS)
    s = re.sub(r'\.\d+$', '', s)[:19]
    for fmt in ('%Y-%m-%dT%H:%M:%S', '%Y-%m-%dT%H:%M', '%Y-%m-%d'):
        try:
            dt = datetime.datetime.strptime(s, fmt)
            return (dt - datetime.datetime(1970, 1, 1)).total_seconds()
        except ValueError:
            pass
    return None

# Parse old ledger entries: agent -> (full_entry_line, next_line, upd_ts_str)
old_entries = {}
if Path(ledger_path).exists():
    lines = Path(ledger_path).read_text().splitlines()
    for i, line in enumerate(lines):
        m = re.match(r'^- \*\*([^*]+)\*\* updated:(\S+)', line)
        if m:
            agent = m.group(1)
            upd = m.group(2)
            next_l = lines[i + 1] if i + 1 < len(lines) and lines[i + 1].startswith('  next:') else ''
            old_entries[agent] = (line, next_l, upd)

# Build fresh entries from current.md files
fresh = {}
state = Path(state_dir)
for d in sorted(state.iterdir()):
    if not d.is_dir() or d.name == 'bin':
        continue
    agent = d.name
    cur = d / 'current.md'
    if not cur.exists():
        continue
    try:
        age = now - cur.stat().st_mtime
    except Exception:
        age = MAX_AGE + 1
    if age > MAX_AGE:
        continue
    text = cur.read_text()
    updated = ''
    m = re.search(r'^UPDATED:\s*(.+)$', text, re.M)
    if m:
        updated = m.group(1).strip()
    next_act = ''
    m = re.search(r'(?:NEXT_ACTION|NEXT ACTION|IN_PROGRESS)[^\n]*\n(.+)', text)
    if m:
        next_act = m.group(1).strip()[:100]
    compact = ''
    m = re.search(r'^LAST_COMPACTED:\s*(.+)$', text, re.M)
    if m:
        compact = m.group(1).strip()
    entry = f"- **{agent}** updated:{updated or '?'} compacted:{compact or 'never'}"
    fresh[agent] = (entry, f"  next: {next_act}" if next_act else '')

# Write merged ledger
out = [
    f"# MASTER-LEDGER -- updated {ts}",
    "# Auto-generated by state-snapshot.sh. Do not edit manually.",
    "",
    "## Agent States",
]
for agent in sorted(fresh):
    entry, next_line = fresh[agent]
    out.append(entry)
    if next_line:
        out.append(next_line)

# Carry forward old entries for agents not in fresh (merge-union)
carried = 0
for agent in sorted(old_entries):
    if agent in fresh:
        continue
    old_line, next_l, upd = old_entries[agent]
    epoch = parse_ts(upd)
    age = (now - epoch) if epoch is not None else MAX_AGE + 1
    if age < MAX_AGE:
        out.append(old_line + ' (carried-forward)')
        if next_l:
            out.append(next_l)
        carried += 1

out += [
    "",
    "## How to find everything",
    "- Agent state: cat ~/.claude/state/<agent>/current.md",
    "- Last compact: tail -30 ~/.claude/state/<agent>/compact-save.log",
    "- Cross-agent notices: notice list --since 24h",
    "- Session restore: handled automatically by SessionStart hook",
    "- Manual find: grep -r 'NEXT_ACTION' ~/.claude/state/",
]
Path(ledger_path).write_text('\n'.join(out) + '\n')
print(f"state-snapshot: {len(fresh)} fresh + {carried} carried-forward")
PY

# Portable atomic lock: prefer flock (Linux); fall back to mkdir-atomic (macOS/no-flock).
cd "$STATE"
LOCK_DIR="${GIT_LOCK}.d"
if [ -d "$LOCK_DIR" ] && find "$LOCK_DIR" -maxdepth 0 -mmin +2 2>/dev/null | grep -q .; then
    rmdir "$LOCK_DIR" 2>/dev/null || true
fi
if command -v flock >/dev/null 2>&1; then
    exec 9>"$GIT_LOCK"
    if ! flock -x -w 20 9 2>/dev/null; then
        echo "$(date -u +%FT%TZ) state-snapshot: could not acquire git lock, skipping commit" >&2; exit 0
    fi
else
    _lk=0
    while ! mkdir "$LOCK_DIR" 2>/dev/null; do
        _lk=$(( _lk + 1 ))
        [ "$_lk" -ge 20 ] && { echo "$(date -u +%FT%TZ) state-snapshot: could not acquire git lock, skipping commit" >&2; exit 0; }
        sleep 1
    done
    trap "rmdir '$LOCK_DIR' 2>/dev/null || true" EXIT INT TERM
fi

git add -A
if ! git diff --cached --quiet; then
    git -c user.email=snapshot@local -c user.name=StateSnapshot commit -qm "snapshot $TS" 2>/dev/null
    for i in 1 2 3; do
        git push -q origin main 2>/dev/null && break
        git pull -q --rebase --autostash origin main 2>/dev/null || git rebase --abort 2>/dev/null
    done
fi
echo "$(date -u +%FT%TZ) state-snapshot: done"
